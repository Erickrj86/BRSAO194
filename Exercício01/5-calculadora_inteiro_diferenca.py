#5- Calculadora de número inteiro

primeiro_valor = int(input("Por favor, informe o primeiro valor: "))
segundo_valor = int(input("Agora, digite o segundo valor: "))
terceiro_valor = int(input("Por favor, informe o terceiro valor: "))
quarto_valor = int(input("Agora, digite o quarto valor: "))

diferenca = ((primeiro_valor * segundo_valor) - (terceiro_valor * quarto_valor))

print(f"DIFERENÇA = {diferenca}")